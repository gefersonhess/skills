canonical_repo_path() {
  cd "$REPO" 2>/dev/null && pwd -P
}

pipeline_repo_from_config() {
  local cfg="$1"
  [ -f "$cfg" ] || return 1
  bash -c '
    set -u
    ISSUES=()
    BRANCHES=()
    source "$1"
    [ -n "${REPO:-}" ] || exit 1
    cd "$REPO" 2>/dev/null && pwd -P
  ' _ "$cfg" 2>/dev/null
}

config_arg_for_pipeline_pid() {
  local pid="$1"
  local -a args=()
  local i

  [ -r "/proc/$pid/cmdline" ] || return 1
  mapfile -d '' -t args < "/proc/$pid/cmdline" 2>/dev/null || return 1

  for i in "${!args[@]}"; do
    if [ "${args[$i]}" = "$SCRIPT_DIR/pipeline.sh" ] || [[ "${args[$i]}" == */pipeline.sh ]]; then
      if [ $((i + 1)) -lt ${#args[@]} ]; then
        echo "${args[$((i + 1))]}"
        return 0
      fi
    fi
  done

  return 1
}

find_same_repo_pipeline_processes() {
  local repo_canonical="$1"
  local pid cfg other_repo

  for pid in $(pgrep -f "$SCRIPT_DIR/pipeline.sh" 2>/dev/null || true); do
    [ -n "$pid" ] || continue
    [ "$pid" != "$$" ] || continue
    [ "$pid" != "${BASHPID:-$$}" ] || continue
    [ "$pid" != "${PPID:-}" ] || continue
    kill -0 "$pid" 2>/dev/null || continue

    cfg=$(config_arg_for_pipeline_pid "$pid" || true)
    [ -n "$cfg" ] || continue
    [ "$cfg" != "$CONFIG_FILE" ] || continue

    other_repo=$(pipeline_repo_from_config "$cfg" || true)
    if [ "$other_repo" = "$repo_canonical" ]; then
      printf 'PID %s config=%s\n' "$pid" "$cfg"
    fi
  done
}

repo_lock_name() {
  local repo_canonical="$1"
  local base hash safe_base
  base=$(basename "$repo_canonical")
  safe_base=$(printf '%s' "$base" | tr -cs 'A-Za-z0-9._-' '-')
  hash=$(printf '%s' "$repo_canonical" | sha256sum | awk '{print $1}' | cut -c1-12)
  printf '%s-%s.lock' "$safe_base" "$hash"
}

acquire_repo_lock() {
  local repo_canonical lock_root lock_name lock_pid same_repo
  repo_canonical=$(canonical_repo_path)
  lock_root="${PIPELINE_LOCK_ROOT:-/tmp/pi-pipeline-locks}"
  lock_name=$(repo_lock_name "$repo_canonical")
  PIPELINE_LOCK_DIR="$lock_root/$lock_name"

  if [ "$ALLOW_CONCURRENT_REPO_PIPELINES" = "1" ]; then
    log "WARNING: same-repo pipeline lock bypassed by ALLOW_CONCURRENT_REPO_PIPELINES=1"
    same_repo=$(find_same_repo_pipeline_processes "$repo_canonical" || true)
    if [ -n "$same_repo" ]; then
      log "WARNING: existing same-repo pipeline process(es) detected despite bypass:"
      while IFS= read -r line; do [ -n "$line" ] && log "  $line"; done <<< "$same_repo"
    fi
    return 0
  fi

  same_repo=$(find_same_repo_pipeline_processes "$repo_canonical" || true)
  if [ -n "$same_repo" ]; then
    log "ERROR: another implementation pipeline is already running for repo $repo_canonical"
    while IFS= read -r line; do [ -n "$line" ] && log "  $line"; done <<< "$same_repo"
    log "Refusing to start. Stop the existing pipeline or set ALLOW_CONCURRENT_REPO_PIPELINES=1 explicitly."
    exit 1
  fi

  mkdir -p "$lock_root"
  if mkdir "$PIPELINE_LOCK_DIR" 2>/dev/null; then
    write_repo_lock_metadata "$repo_canonical"
    log "Acquired repo pipeline lock: $PIPELINE_LOCK_DIR"
    return 0
  fi

  lock_pid=""
  [ -f "$PIPELINE_LOCK_DIR/pid" ] && lock_pid=$(cat "$PIPELINE_LOCK_DIR/pid" 2>/dev/null || true)
  if [ -n "$lock_pid" ] && kill -0 "$lock_pid" 2>/dev/null; then
    log "ERROR: repo pipeline lock is held for $repo_canonical"
    [ -f "$PIPELINE_LOCK_DIR/metadata" ] && while IFS= read -r line; do log "  $line"; done < "$PIPELINE_LOCK_DIR/metadata"
    log "Refusing to start. Stop the existing pipeline or set ALLOW_CONCURRENT_REPO_PIPELINES=1 explicitly."
    exit 1
  fi

  log "Removing stale repo pipeline lock: $PIPELINE_LOCK_DIR"
  rm -rf "$PIPELINE_LOCK_DIR"
  if mkdir "$PIPELINE_LOCK_DIR" 2>/dev/null; then
    write_repo_lock_metadata "$repo_canonical"
    log "Acquired repo pipeline lock: $PIPELINE_LOCK_DIR"
    return 0
  fi

  log "ERROR: could not acquire repo pipeline lock: $PIPELINE_LOCK_DIR"
  exit 1
}

write_repo_lock_metadata() {
  local repo_canonical="$1"
  {
    echo "pid=$$"
    echo "repo=$repo_canonical"
    echo "config=$CONFIG_FILE"
    echo "log_dir=$LOG_DIR"
    echo "issues=${ISSUES[*]}"
    echo "started_at=${PIPELINE_START:-}"
    echo "state=${PIPELINE_LOCK_STATE:-running}"
    echo "pipeline_id=${PIPELINE_ID:-}"
    echo "status_file=$LOG_DIR/status.json"
    echo "log_file=$LOG_DIR/loop.log"
    echo "control_file=$LOG_DIR/control"
  } > "$PIPELINE_LOCK_DIR/metadata"
  echo "$$" > "$PIPELINE_LOCK_DIR/pid"
}

update_lock_state() {
  local new_state="$1"
  PIPELINE_LOCK_STATE="$new_state"
  [ -n "${PIPELINE_LOCK_DIR:-}" ] && [ -d "$PIPELINE_LOCK_DIR" ] || return 0
  local canonical
  canonical=$(canonical_repo_path)
  write_repo_lock_metadata "$canonical"
}

release_repo_lock() {
  local lock_pid
  [ -n "${PIPELINE_LOCK_DIR:-}" ] || return 0
  [ -d "$PIPELINE_LOCK_DIR" ] || return 0
  lock_pid=""
  [ -f "$PIPELINE_LOCK_DIR/pid" ] && lock_pid=$(cat "$PIPELINE_LOCK_DIR/pid" 2>/dev/null || true)
  if [ "$lock_pid" = "$$" ]; then
    rm -rf "$PIPELINE_LOCK_DIR"
    log "Released repo pipeline lock: $PIPELINE_LOCK_DIR"
  fi
}

release_repo_lock_on_exit() {
  release_repo_lock
}
