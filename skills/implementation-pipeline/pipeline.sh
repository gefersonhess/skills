handle_issue_failure() {
  local issue="$1"
  local reason="$2"

  ISSUES_SKIPPED+=("$issue")

  if [ "$CONTINUE_ON_FAILURE" = "1" ]; then
    log "  Continuing after #$issue failure because CONTINUE_ON_FAILURE=1: $reason"
    return 0
  fi

  set_phase "blocked"
  PIPELINE_TERMINAL_STATE="blocked"
  log "  Stopping before the next issue. Sequential mode requires #$issue to be resolved first: $reason"
  write_status "blocked"
  return 1
}

process_tracker_checkpoint() {
  local issue="$1" branch_spec="$2"
  local child_csv="${branch_spec#tracker:}"
  local child state title
  local open_children=()

  if [ -z "$child_csv" ] || [ "$child_csv" = "$branch_spec" ]; then
    handle_issue_failure "$issue" "invalid tracker checkpoint branch spec: $branch_spec"
    return 1
  fi

  log "[tracker] Verifying split tracker #$issue children: $child_csv"

  IFS=',' read -r -a children <<< "$child_csv"
  for child in "${children[@]}"; do
    child="${child//#/}"
    child="${child//[[:space:]]/}"
    if [ -z "$child" ]; then
      continue
    fi

    state=$(gh_issue_state_with_retry "$REPO" "$child")
    if [ "$state" != "CLOSED" ]; then
      open_children+=("#$child:$state")
    fi
  done

  if [ ${#open_children[@]} -gt 0 ]; then
    handle_issue_failure "$issue" "tracker children not complete: ${open_children[*]}"
    return 1
  fi

  state=$(gh_issue_state_with_retry "$REPO" "$issue")
  title=$(cd "$REPO" && gh issue view "$issue" --json title --jq .title 2>/dev/null || echo "")

  if [ "$state" = "CLOSED" ]; then
    log "  Tracker #$issue already closed ✓"
  elif [ "$state" = "OPEN" ]; then
    local comment_file
    comment_file="$LOG_DIR/tracker-${issue}-complete.md"
    {
      echo "Tracker checkpoint complete."
      echo ""
      echo "All split child issues are closed:"
      echo ""
      for child in "${children[@]}"; do
        child="${child//#/}"
        child="${child//[[:space:]]/}"
        [ -n "$child" ] && echo "- #$child"
      done
      echo ""
      echo "Downstream issues may treat this tracker dependency as satisfied."
    } > "$comment_file"
    (cd "$REPO" && gh issue comment "$issue" --body-file "$comment_file") >/dev/null
    (cd "$REPO" && gh issue close "$issue" --comment "Closing tracker checkpoint: all split child issues are complete.") >/dev/null
    log "  Closed tracker #$issue ${title:+— $title} ✓"
  else
    handle_issue_failure "$issue" "cannot verify tracker state for #$issue (state=$state)"
    return 1
  fi

  mark_issue_completed "$issue"
  write_status "running"
  return 0
}

