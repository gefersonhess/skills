validate_config() {
  local errors=0

  # Required fields
  for var in REPO WORKTREE_BASE OWNER_REPO AI_REVIEW_PROVIDER AI_REVIEW_API_BASE; do
    if [ -z "${!var:-}" ]; then
      echo "ERROR: Required config variable $var is not set." >&2
      errors=$((errors + 1))
    fi
  done

  # Arrays must exist and be non-empty
  if [ ${#ISSUES[@]} -eq 0 ]; then
    echo "ERROR: ISSUES array is empty." >&2
    errors=$((errors + 1))
  fi
  if [ ${#BRANCHES[@]} -eq 0 ]; then
    echo "ERROR: BRANCHES array is empty." >&2
    errors=$((errors + 1))
  fi
  if [ ${#ISSUES[@]} -ne ${#BRANCHES[@]} ]; then
    echo "ERROR: ISSUES (${#ISSUES[@]}) and BRANCHES (${#BRANCHES[@]}) arrays must be same length." >&2
    errors=$((errors + 1))
  fi
  for issue in "${ISSUES[@]}"; do
    if ! [[ "$issue" =~ ^[0-9]+$ ]]; then
      echo "ERROR: ISSUES entries must be numeric GitHub issue numbers (got: $issue)." >&2
      errors=$((errors + 1))
    fi
  done

  # Repo must exist
  if [ ! -d "$REPO/.git" ] && [ ! -f "$REPO/.git" ]; then
    echo "ERROR: REPO ($REPO) is not a git repository." >&2
    errors=$((errors + 1))
  fi

  # MERGE_STRATEGY must be valid
  case "${MERGE_STRATEGY:-squash}" in
    squash|merge|rebase) ;;
    *) echo "ERROR: MERGE_STRATEGY must be squash, merge, or rebase (got: $MERGE_STRATEGY)" >&2; errors=$((errors + 1)) ;;
  esac

  # AI_REVIEW_PROVIDER must be valid
  case "${AI_REVIEW_PROVIDER:-}" in
    coderabbit|ghe-pr-bot) ;;
    *) echo "ERROR: AI_REVIEW_PROVIDER must be coderabbit or ghe-pr-bot (got: ${AI_REVIEW_PROVIDER:-})" >&2; errors=$((errors + 1)) ;;
  esac

  # Boolean toggles must be 0 or 1
  for var in SKIP_REVIEW SKIP_BOT SKIP_SCOPE_GATE NO_MERGE CONTINUE_ON_FAILURE ALLOW_CONCURRENT_REPO_PIPELINES; do
    if [ "${!var:-0}" != "0" ] && [ "${!var:-0}" != "1" ]; then
      echo "ERROR: $var must be 0 or 1 (got: ${!var:-})" >&2
      errors=$((errors + 1))
    fi
  done

  # Timeouts and polling intervals must be positive integers.
  for var in TIMEOUT_IMPL TIMEOUT_REVIEW TIMEOUT_BOT TIMEOUT_CI TIMEOUT_GATE HANDOFF_POLL_SECONDS CI_POLL_SECONDS CI_RETRY_LIMIT PAUSE_POLL_SECONDS DEAD_AGENT_FLUSH_SECONDS; do
    if ! [[ "${!var:-0}" =~ ^[0-9]+$ ]] || [ "${!var:-0}" -eq 0 ]; then
      echo "ERROR: $var must be a positive integer (got: ${!var:-})" >&2
      errors=$((errors + 1))
    fi
  done

  # Final status settle is not a poll loop; zero is valid and means no delay.
  if ! [[ "${FINAL_STATUS_SETTLE_SECONDS:-0}" =~ ^[0-9]+$ ]]; then
    echo "ERROR: FINAL_STATUS_SETTLE_SECONDS must be a non-negative integer (got: ${FINAL_STATUS_SETTLE_SECONDS:-})" >&2
    errors=$((errors + 1))
  fi

  # pi must be available
  if ! command -v pi >/dev/null 2>&1; then
    echo "ERROR: 'pi' command not found in PATH." >&2
    errors=$((errors + 1))
  fi

  # gh must be available
  if ! command -v gh >/dev/null 2>&1; then
    echo "ERROR: 'gh' command not found in PATH." >&2
    errors=$((errors + 1))
  fi

  if [ $errors -gt 0 ]; then
    echo "Config validation failed with $errors error(s)." >&2
    return 1
  fi
  return 0
}

