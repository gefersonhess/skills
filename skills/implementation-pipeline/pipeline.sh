wait_for_ci() {
  local pr="$1"
  local timeout="${2:-$TIMEOUT_CI}"
  local elapsed=0
  local poll_seconds="${CI_POLL_SECONDS:-10}"

  log "    Polling CI (max ${timeout}s)..." >&2
  while [ $elapsed -lt "$timeout" ]; do
    local ci_json pending failures
    ci_json=$(gh pr view "$pr" --json statusCheckRollup 2>/dev/null) || ci_json='{"statusCheckRollup":[]}'

    pending=$(jq --arg provider "$AI_REVIEW_PROVIDER" '
      def authoritative:
        if $provider == "ghe-pr-bot" then
          select((.name // .context // "") != "SonarQube")
        else
          .
        end;
      [.statusCheckRollup[] | authoritative |
        select((.status != null and .status != "COMPLETED") or (.state != null and (.state == "PENDING" or .state == "EXPECTED")))
      ] | length
    ' <<< "$ci_json" 2>/dev/null) || pending=999

    if [ "${pending:-999}" = "0" ]; then
      failures=$(jq --arg provider "$AI_REVIEW_PROVIDER" '
        def authoritative:
          if $provider == "ghe-pr-bot" then
            select((.name // .context // "") != "SonarQube")
          else
            .
          end;
        [.statusCheckRollup[] | authoritative |
          select((.conclusion != null and (.conclusion == "FAILURE" or .conclusion == "CANCELLED" or .conclusion == "TIMED_OUT" or .conclusion == "ACTION_REQUIRED")) or (.state != null and (.state == "FAILURE" or .state == "ERROR")))
        ] | length
      ' <<< "$ci_json" 2>/dev/null) || failures=999
      echo "${failures:-0}"
      return 0
    fi

    sleep "$poll_seconds"
    elapsed=$((elapsed + poll_seconds))
    if (( elapsed % 120 == 0 )); then
      log "    CI: ${pending:-?} authoritative checks pending (${elapsed}s)" >&2
    fi
  done

  log "    CI timed out after ${timeout}s" >&2
  echo "timeout"
  return 1
}

