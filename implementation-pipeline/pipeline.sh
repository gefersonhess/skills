  cat >&2 <<EOF
Usage: $(basename "$0") <config-file>

The config file must define:
  REPO             — path to canonical repo checkout
  WORKTREE_BASE    — base path for worktrees
  OWNER_REPO       — GitHub owner/repo (e.g. "org/repo-name")
  AI_REVIEW_PROVIDER  — ai-pr-review-loop provider (coderabbit or ghe-pr-bot)
  AI_REVIEW_API_BASE  — GitHub API base URL for the provider
  ISSUES           — bash array of issue numbers
  BRANCHES         — bash array of branch names (same length as ISSUES)

Optional (have defaults):
  BASE_BRANCH          — base branch (auto-detected if unset)
  MERGE_STRATEGY       — squash (default), merge, rebase
  REVIEW_LOOP_COUNT    — max bot review rounds (default: 5)
  TIMEOUT_IMPL         — implementation timeout in seconds (default: 2400)
  TIMEOUT_REVIEW       — self-review timeout in seconds (default: 1200)
  TIMEOUT_BOT          — bot review timeout in seconds (default: 7200)
  TIMEOUT_CI           — CI polling timeout in seconds (default: 600)
  TIMEOUT_GATE         — scope gate timeout in seconds (default: 120)
  LOCAL_CODERABBIT_PRECHECK — 1 to run local CodeRabbit CLI before PR for coderabbit provider (default: 1)
  SKIP_REVIEW          — 1 to skip self-review phase (default: 0)
  SKIP_BOT             — 1 to skip bot review phase (default: 0)
  SKIP_SCOPE_GATE      — 1 to skip scope gate (default: 0)
  FORCE_ISSUES         — comma-separated issues to bypass scope gate
  NO_MERGE             — 1 to stop after review without merging (default: 0)
  LOG_DIR              — override log directory (default: /tmp/impl-pipeline-<timestamp>)
  CONTINUE_ON_FAILURE  — set 1 only for independent best-effort batches (default: 0)
  IMPL_SKILL           — skill path for implementation (default: design-first-implementation)
  REVIEW_SKILL         — skill path for self-review (default: targeted-pr-review)
  BOT_SKILL            — skill path for bot review (default: ai-pr-review-loop)
  GHE_API              — deprecated fallback for AI_REVIEW_API_BASE
  EXTRA_IMPL_CONTEXT   — extra text appended to implementation prompt
EOF
  exit 1
}

validate_config() {
  local errors=0

  # Required fields
  for var in REPO WORKTREE_BASE OWNER_REPO AI_REVIEW_PROVIDER AI_REVIEW_API_BASE; do
    if [ -z "${!var:-}" ]; then
      echo "ERROR: Required config variable $var is not set." >&2
      errors=$((errors + 1))
    fi
  done

  # Arrays must exist and be non-empty
  if [ ${#ISSUES[@]} -eq 0 ]; then
    echo "ERROR: ISSUES array is empty." >&2
    errors=$((errors + 1))
  fi
  if [ ${#BRANCHES[@]} -eq 0 ]; then
    echo "ERROR: BRANCHES array is empty." >&2
    errors=$((errors + 1))
  fi
  if [ ${#ISSUES[@]} -ne ${#BRANCHES[@]} ]; then
    echo "ERROR: ISSUES (${#ISSUES[@]}) and BRANCHES (${#BRANCHES[@]}) arrays must be same length." >&2
    errors=$((errors + 1))
  fi

  # Repo must exist
  if [ ! -d "$REPO/.git" ] && [ ! -f "$REPO/.git" ]; then
    echo "ERROR: REPO ($REPO) is not a git repository." >&2
    errors=$((errors + 1))
  fi

  # MERGE_STRATEGY must be valid
  case "${MERGE_STRATEGY:-squash}" in
    squash|merge|rebase) ;;
    *) echo "ERROR: MERGE_STRATEGY must be squash, merge, or rebase (got: $MERGE_STRATEGY)" >&2; errors=$((errors + 1)) ;;
  esac

  # AI_REVIEW_PROVIDER must be valid
  case "${AI_REVIEW_PROVIDER:-}" in
    coderabbit|ghe-pr-bot) ;;
    *) echo "ERROR: AI_REVIEW_PROVIDER must be coderabbit or ghe-pr-bot (got: ${AI_REVIEW_PROVIDER:-})" >&2; errors=$((errors + 1)) ;;
  esac

  # Boolean toggles must be 0 or 1
  for var in SKIP_REVIEW SKIP_BOT SKIP_SCOPE_GATE NO_MERGE CONTINUE_ON_FAILURE; do
    if [ "${!var:-0}" != "0" ] && [ "${!var:-0}" != "1" ]; then
      echo "ERROR: $var must be 0 or 1 (got: ${!var:-})" >&2
      errors=$((errors + 1))
    fi
  done

  # Timeouts must be positive integers
  for var in TIMEOUT_IMPL TIMEOUT_REVIEW TIMEOUT_BOT TIMEOUT_CI TIMEOUT_GATE; do
    if ! [[ "${!var:-0}" =~ ^[0-9]+$ ]] || [ "${!var:-0}" -eq 0 ]; then
      echo "ERROR: $var must be a positive integer (got: ${!var:-})" >&2
      errors=$((errors + 1))
    fi
  done

  # pi must be available
  if ! command -v pi >/dev/null 2>&1; then
    echo "ERROR: 'pi' command not found in PATH." >&2
    errors=$((errors + 1))
  fi

  # gh must be available
  if ! command -v gh >/dev/null 2>&1; then
    echo "ERROR: 'gh' command not found in PATH." >&2
    errors=$((errors + 1))
  fi

  if [ $errors -gt 0 ]; then
    echo "Config validation failed with $errors error(s)." >&2
    return 1
  fi
  return 0
}

