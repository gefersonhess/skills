# ── Process management ───────────────────────────────────────────────────────

CHILD_PIDS=()

cleanup() {
  local exit_code=$?
  log "Pipeline interrupted (exit=$exit_code) — killing child processes"
  for pid in "${CHILD_PIDS[@]:-}"; do
    if [ -n "$pid" ] && kill -0 "$pid" 2>/dev/null; then
      # Try process group first, fall back to single PID
      kill -TERM -- -"$pid" 2>/dev/null || kill -TERM "$pid" 2>/dev/null || true
      log "  Sent TERM to PID $pid"
    fi
  done
  # Grace period then force kill
  sleep 5
  for pid in "${CHILD_PIDS[@]:-}"; do
    if [ -n "$pid" ] && kill -0 "$pid" 2>/dev/null; then
      kill -9 "$pid" 2>/dev/null || true
      log "  Force-killed PID $pid"
    fi
  done
  write_status "killed"
  log "Cleanup complete. Log: $LOG_DIR/loop.log"
  exit "$exit_code"
}
trap cleanup INT TERM

# ── Utility functions ────────────────────────────────────────────────────────

log() {
  echo "[$(date -u +%Y-%m-%dT%H:%M:%SZ)] $*" | tee -a "$LOG_DIR/loop.log"
}

write_status() {
  local state="$1"
  cat > "$LOG_DIR/status.json.tmp" <<EOF
{
  "pipeline_state": "$state",
  "version": "$VERSION",
  "config_file": "$CONFIG_FILE",
  "started_at": "$PIPELINE_START",
  "current_issue": ${CURRENT_ISSUE:-null},
  "current_phase": "${CURRENT_PHASE:-}",
  "current_pr": ${CURRENT_PR:-null},
  "current_agent_pid": ${CURRENT_AGENT_PID:-null},
  "issues_completed": [$(IFS=,; echo "${ISSUES_COMPLETED[*]:-}")],
  "issues_skipped": [$(IFS=,; echo "${ISSUES_SKIPPED[*]:-}")],
  "issues_remaining": [$(IFS=,; echo "${ISSUES_REMAINING[*]:-}")],
  "last_update": "$(date -u +%Y-%m-%dT%H:%M:%SZ)"
}
EOF
  mv "$LOG_DIR/status.json.tmp" "$LOG_DIR/status.json"
}

check_control() {
  if [ -f "$LOG_DIR/control" ]; then
    local cmd
    cmd=$(cat "$LOG_DIR/control" 2>/dev/null || echo "")
    : > "$LOG_DIR/control"
    echo "$cmd"
  else
    echo ""
  fi
}

wait_for_handoff() {
  local handoff_file="$1"
  local timeout="$2"
  local agent_pid="${3:-}"
  local elapsed=0

  while [ $elapsed -lt "$timeout" ]; do
    # File must exist AND be non-empty (guards against partial writes)
    if [ -f "$handoff_file" ] && [ -s "$handoff_file" ]; then
      return 0
    fi

    # Dead-PID detection
    if [ -n "$agent_pid" ] && ! kill -0 "$agent_pid" 2>/dev/null; then
      # Grace period for filesystem flush
      sleep 5
      if [ -f "$handoff_file" ] && [ -s "$handoff_file" ]; then
        return 0
      fi
      log "    Agent PID $agent_pid died without writing handoff"
      return 1
    fi

    sleep 30
    elapsed=$((elapsed + 30))

    # Heartbeat every 5 minutes
    if (( elapsed % 300 == 0 )); then
      log "    Waiting for handoff... ${elapsed}s / ${timeout}s"
    fi
  done

  # Final check
  [ -f "$handoff_file" ] && [ -s "$handoff_file" ]
}

kill_agent() {
  local pid="$1"
  if kill -0 "$pid" 2>/dev/null; then
    kill -TERM "$pid" 2>/dev/null || true
    sleep 3
    if kill -0 "$pid" 2>/dev/null; then
      kill -9 "$pid" 2>/dev/null || true
    fi
  fi
}

extract_pr_number() {
  local worktree="$1"
  local handoff="${2:-}"
  local pr_num=""

  # Authoritative: gh CLI on the branch
  pr_num=$(cd "$worktree" && gh pr view --json number -q .number 2>/dev/null) || true
  if [ -n "$pr_num" ] && [ "$pr_num" != "null" ]; then
    echo "$pr_num"
    return 0
  fi

  # Fallback: parse handoff
  if [ -n "$handoff" ] && [ -f "$handoff" ]; then
    pr_num=$(grep -oP '(?:PR\s*#|pull/|number:\s*)(\d+)' "$handoff" 2>/dev/null | grep -oP '\d+' | head -1) || true
    if [ -n "$pr_num" ]; then
      echo "$pr_num"
      return 0
    fi
  fi

  return 1
}

verify_local_coderabbit_precheck() {
  local handoff="$1"

  if [ "${AI_REVIEW_PROVIDER:-}" != "coderabbit" ] || [ "${LOCAL_CODERABBIT_PRECHECK:-1}" != "1" ]; then
    return 0
  fi

  if [ ! -s "$handoff" ]; then
    log "  ERROR: Missing implementation handoff; cannot verify local CodeRabbit precheck."
    return 1
  fi

  if ! grep -Eiq "local[[:space:]-]+coderabbit|coderabbit review" "$handoff"; then
    log "  ERROR: Implementation handoff does not document the required local CodeRabbit precheck."
    return 1
  fi

  if grep -Eiq "coderabbit.*(not run|skipped|unavailable|failed|error)|doctor.*(fail|error)|precheck.*(not run|skipped|failed|unavailable)" "$handoff"; then
    log "  ERROR: Local CodeRabbit precheck is documented as missing/failed."
    return 1
  fi

  if ! grep -Eiq "(0 findings|findings:[[:space:]]*0|findings[[:space:]]*\|?[[:space:]]*0|all (findings )?addressed|addressed in-place|clean|review completed.*0)" "$handoff"; then
    log "  ERROR: Local CodeRabbit precheck lacks a clear clean/all-addressed result."
    return 1
  fi

  log "  Local CodeRabbit precheck documented ✓"
  return 0
}

wait_for_ci() {
  local pr="$1"
  local timeout="${2:-$TIMEOUT_CI}"
  local elapsed=0

  log "    Polling CI (max ${timeout}s)..." >&2
  while [ $elapsed -lt "$timeout" ]; do
    local pending
    pending=$(gh pr view "$pr" --json statusCheckRollup -q \
      '[.statusCheckRollup[] | select((.status != null and .status != "COMPLETED") or (.state != null and (.state == "PENDING" or .state == "EXPECTED"))) ] | length' 2>/dev/null) || true

    if [ "${pending:-999}" = "0" ]; then
      local failures
      failures=$(gh pr view "$pr" --json statusCheckRollup -q \
        '[.statusCheckRollup[] | select((.conclusion != null and (.conclusion == "FAILURE" or .conclusion == "CANCELLED" or .conclusion == "TIMED_OUT" or .conclusion == "ACTION_REQUIRED")) or (.state != null and (.state == "FAILURE" or .state == "ERROR"))) ] | length' 2>/dev/null) || true
      echo "${failures:-0}"
      return 0
    fi

    sleep 30
    elapsed=$((elapsed + 30))
    if (( elapsed % 120 == 0 )); then
      log "    CI: ${pending:-?} checks pending (${elapsed}s)" >&2
    fi
  done

  log "    CI timed out after ${timeout}s" >&2
  echo "timeout"
  return 1
}

cleanup_worktree() {
  local wt_path="$1"
  if [ -d "$wt_path" ]; then
    cd "$REPO" || return
    git worktree remove "$wt_path" --force 2>/dev/null || rm -rf "$wt_path"
    git worktree prune 2>/dev/null || true
  fi
}

handle_issue_failure() {
  local issue="$1"
  local reason="$2"

  ISSUES_SKIPPED+=("$issue")

  if [ "$CONTINUE_ON_FAILURE" = "1" ]; then
    log "  Continuing after #$issue failure because CONTINUE_ON_FAILURE=1: $reason"
    return 0
  fi

  CURRENT_PHASE="blocked"
  PIPELINE_TERMINAL_STATE="blocked"
  log "  Stopping before the next issue. Sequential mode requires #$issue to be resolved first: $reason"
  write_status "blocked"
  return 1
}

