# ── Pipeline state ───────────────────────────────────────────────────────────

PIPELINE_START="$(date -u +%Y-%m-%dT%H:%M:%SZ)"
ISSUES_COMPLETED=()
ISSUES_SKIPPED=()
ISSUES_REMAINING=("${ISSUES[@]}")
CURRENT_ISSUE=""
CURRENT_PHASE=""
CURRENT_PR=""
CURRENT_AGENT_PID=""
PIPELINE_TERMINAL_STATE="completed"

write_status "running"

log "═══════════════════════════════════════════════════════════════"
log "Implementation Pipeline v${VERSION}"
log "═══════════════════════════════════════════════════════════════"
log "Config:    $CONFIG_FILE"
log "Repo:      $REPO"
log "Issues:    ${ISSUES[*]}"
log "Strategy:  $MERGE_STRATEGY"
log "Timeouts:  impl=${TIMEOUT_IMPL}s rev=${TIMEOUT_REVIEW}s bot=${TIMEOUT_BOT}s ci=${TIMEOUT_CI}s"
log "Flags:     review=${SKIP_REVIEW:+SKIP}${SKIP_REVIEW:-on} bot=${SKIP_BOT:+SKIP}${SKIP_BOT:-on} gate=${SKIP_SCOPE_GATE:+SKIP}${SKIP_SCOPE_GATE:-on} merge=${NO_MERGE:+NO}${NO_MERGE:-on} continue_on_failure=$CONTINUE_ON_FAILURE local_coderabbit=$LOCAL_CODERABBIT_PRECHECK"
log "Log dir:   $LOG_DIR"
log "═══════════════════════════════════════════════════════════════"

# ── Main loop ────────────────────────────────────────────────────────────────

for i in "${!ISSUES[@]}"; do
  ISSUE="${ISSUES[$i]}"
  BRANCH="${BRANCHES[$i]}"
  WORKTREE="$WORKTREE_BASE/$BRANCH"
  CURRENT_ISSUE="$ISSUE"
  CURRENT_PR=""
  SCOPE_WARNING=""

  # Remove from remaining
  ISSUES_REMAINING=("${ISSUES_REMAINING[@]:1}")

  # ── Control check ─────────────────────────────────────
  CTRL=$(check_control)
  case "$CTRL" in
    abort)
      log "ABORT received. Stopping."
      write_status "aborted"
      break
      ;;
    pause)
      log "PAUSE received. Waiting for 'resume'..."
      while true; do
        sleep 10
        CTRL=$(check_control)
        [ "$CTRL" = "resume" ] && break
        [ "$CTRL" = "abort" ] && { log "ABORT during pause."; write_status "aborted"; exit 0; }
      done
      log "Resumed."
      ;;
    skip)
      log "SKIP received. Skipping #$ISSUE."
      ISSUES_SKIPPED+=("$ISSUE")
      continue
      ;;
  esac

  log ""
  log "══════════════════════════════════════"
  log "ISSUE #$ISSUE — $BRANCH"
  log "══════════════════════════════════════"

  # ── Phase 1: Worktree setup ───────────────────────────
  CURRENT_PHASE="worktree-setup"
  write_status "running"
  log "[1/5] Worktree setup"
  if ! cd "$REPO"; then
    handle_issue_failure "$ISSUE" "cannot cd to repo $REPO" || break
    continue
  fi
  git fetch origin "$BASE_BRANCH" --quiet

  if [ -d "$WORKTREE" ]; then
    # Check for existing PR (don't destroy in-progress work)
    EXISTING_PR=$(cd "$WORKTREE" && gh pr view --json number -q .number 2>/dev/null) || true
    if [ -n "$EXISTING_PR" ] && [ "$EXISTING_PR" != "null" ]; then
      log "  PR #$EXISTING_PR already exists for $BRANCH."
      handle_issue_failure "$ISSUE" "existing open PR #$EXISTING_PR must be merged or closed before continuing" || break
      continue
    fi
    if ! cd "$WORKTREE"; then
      handle_issue_failure "$ISSUE" "cannot cd to existing worktree $WORKTREE" || break
      continue
    fi
    git fetch origin "$BASE_BRANCH" --quiet
    git reset --hard "origin/$BASE_BRANCH"
  elif [ -x "$REPO/scripts/setup-worktree.sh" ]; then
    "$REPO/scripts/setup-worktree.sh" "$BRANCH" || {
      log "  setup-worktree.sh failed, creating manually"
      git branch -D "$BRANCH" 2>/dev/null || true
      git worktree add "$WORKTREE" -b "$BRANCH" "origin/$BASE_BRANCH"
      cd "$WORKTREE" && [ -f Makefile ] && make sync
    }
  else
    git branch -D "$BRANCH" 2>/dev/null || true
    git worktree add "$WORKTREE" -b "$BRANCH" "origin/$BASE_BRANCH"
    cd "$WORKTREE" && [ -f Makefile ] && make sync
  fi

  if ! cd "$WORKTREE"; then
    handle_issue_failure "$ISSUE" "worktree setup did not produce $WORKTREE" || break
    continue
  fi
  log "  Ready: $WORKTREE"

  # ── Phase 0: Scope gate ───────────────────────────────
  if [ "$SKIP_SCOPE_GATE" = "1" ]; then
    log "[0] Scope gate: SKIPPED"
  elif echo ",$FORCE_ISSUES," | grep -q ",$ISSUE,"; then
    log "[0] Scope gate: BYPASSED (FORCE_ISSUES)"
  else
    CURRENT_PHASE="scope-gate"
    write_status "running"
    log "[0] Scope gate"
    GATE_ID="gate-${ISSUE}-$(date +%s)"
    GATE_FILE="/tmp/${GATE_ID}-verdict.txt"
    GATE_PROMPT_FILE="$LOG_DIR/${GATE_ID}-prompt.md"

    generate_gate_prompt "$ISSUE" "$GATE_FILE" > "$GATE_PROMPT_FILE"

    cd "$WORKTREE" || continue
    nohup pi --approve \
      --session-id "$GATE_ID" \
      -p "@$GATE_PROMPT_FILE" \
      > "$LOG_DIR/${GATE_ID}.log" 2>&1 &
    GATE_PID=$!
    CHILD_PIDS+=("$GATE_PID")

    if ! wait_for_handoff "$GATE_FILE" "$TIMEOUT_GATE" "$GATE_PID"; then
      log "  Gate timed out. Proceeding by default."
      kill_agent "$GATE_PID"
    else
      GATE_VERDICT=$(head -1 "$GATE_FILE" 2>/dev/null | tr -d '\n')
      case "$GATE_VERDICT" in
        proceed)
          log "  PROCEED"
          ;;
        proceed-with-warning:*)
          SCOPE_WARNING="${GATE_VERDICT#proceed-with-warning:}"
          log "  PROCEED WITH WARNING: $SCOPE_WARNING"
          ;;
        skip:*)
          log "  SKIP: ${GATE_VERDICT#skip:}"
          handle_issue_failure "$ISSUE" "scope gate skip: ${GATE_VERDICT#skip:}" || break
          continue
          ;;
        blocker:*)
          log "  BLOCKER: ${GATE_VERDICT#blocker:}"
          handle_issue_failure "$ISSUE" "scope gate blocker: ${GATE_VERDICT#blocker:}" || break
          continue
          ;;
        *)
          log "  Unexpected verdict: '$GATE_VERDICT'. Proceeding."
          ;;
      esac
    fi
  fi

  # ── Phase 2: Implementation ───────────────────────────
  CURRENT_PHASE="implementation"
  write_status "running"
  log "[2/5] Implementation"
  IMPL_ID="impl-${ISSUE}-$(date +%s)"
  IMPL_HANDOFF="/tmp/${IMPL_ID}-handoff.md"
  IMPL_PROMPT_FILE="$LOG_DIR/${IMPL_ID}-prompt.md"

  generate_impl_prompt "$ISSUE" "$WORKTREE" "$BRANCH" "$IMPL_HANDOFF" "$SCOPE_WARNING" > "$IMPL_PROMPT_FILE"

  cd "$WORKTREE" || continue
  nohup pi --approve \
    --session-id "$IMPL_ID" \
    --skill "$IMPL_SKILL_PATH" \
    -p "@$IMPL_PROMPT_FILE" \
    > "$LOG_DIR/${IMPL_ID}.log" 2>&1 &
  IMPL_PID=$!
  CHILD_PIDS+=("$IMPL_PID")
  CURRENT_AGENT_PID="$IMPL_PID"
  write_status "running"
  log "  Agent PID $IMPL_PID (session: $IMPL_ID)"

  if ! wait_for_handoff "$IMPL_HANDOFF" "$TIMEOUT_IMPL" "$IMPL_PID"; then
    log "  ERROR: Timed out or died after ${TIMEOUT_IMPL}s."
    kill_agent "$IMPL_PID"
    handle_issue_failure "$ISSUE" "implementation timed out or died" || break
    continue
  fi
  log "  Done. Verifying local pre-PR CodeRabbit precheck..."
  if ! verify_local_coderabbit_precheck "$IMPL_HANDOFF"; then
    handle_issue_failure "$ISSUE" "local CodeRabbit precheck missing or failed" || break
    continue
  fi
  log "  Extracting PR number..."

  CURRENT_PR=$(extract_pr_number "$WORKTREE" "$IMPL_HANDOFF") || true
  if [ -z "$CURRENT_PR" ]; then
    log "  ERROR: No PR number found."
    handle_issue_failure "$ISSUE" "implementation did not produce a PR" || break
    continue
  fi
  log "  PR #$CURRENT_PR opened"
  write_status "running"

  # Control check between phases
  CTRL=$(check_control)
  if [ "$CTRL" = "skip" ]; then
    log "  SKIP received. PR #$CURRENT_PR left open."
    ISSUES_SKIPPED+=("$ISSUE")
    continue
  fi

  # ── Phase 3: Self-review ──────────────────────────────
  if [ "$SKIP_REVIEW" = "1" ]; then
    log "[3/5] Self-review: SKIPPED"
  else
    CURRENT_PHASE="self-review"
    write_status "running"
    log "[3/5] Self-review"
    REV_ID="review-${ISSUE}-$(date +%s)"
    REV_HANDOFF="/tmp/${REV_ID}-handoff.md"
    REV_PROMPT_FILE="$LOG_DIR/${REV_ID}-prompt.md"

    generate_review_prompt "$CURRENT_PR" "$WORKTREE" "$BRANCH" "$REV_HANDOFF" > "$REV_PROMPT_FILE"

    cd "$WORKTREE" || continue
    nohup pi --approve \
      --session-id "$REV_ID" \
      --skill "$REVIEW_SKILL_PATH" \
      -p "@$REV_PROMPT_FILE" \
      > "$LOG_DIR/${REV_ID}.log" 2>&1 &
    REV_PID=$!
    CHILD_PIDS+=("$REV_PID")
    CURRENT_AGENT_PID="$REV_PID"
    write_status "running"
    log "  Agent PID $REV_PID (session: $REV_ID)"

    if ! wait_for_handoff "$REV_HANDOFF" "$TIMEOUT_REVIEW" "$REV_PID"; then
      log "  WARNING: Timed out or died. Continuing."
      kill_agent "$REV_PID"
    else
      log "  Done"
    fi
  fi

  # Control check
  CTRL=$(check_control)
  if [ "$CTRL" = "skip" ]; then
    log "  SKIP received. PR #$CURRENT_PR left open."
    ISSUES_SKIPPED+=("$ISSUE")
    continue
  fi

  # ── Phase 4: Bot review ───────────────────────────────
  if [ "$SKIP_BOT" = "1" ]; then
    log "[4/5] Bot review: SKIPPED"
  else
    CURRENT_PHASE="bot-review"
    write_status "running"
    log "[4/5] Bot review via $AI_REVIEW_PROVIDER (max $REVIEW_LOOP_COUNT rounds)"
    BOT_ID="ai-pr-review-loop-${AI_REVIEW_PROVIDER}-${CURRENT_PR}-$(date +%s)"
    BOT_HANDOFF="/tmp/${BOT_ID}-handoff.md"
    BOT_PROMPT_FILE="$LOG_DIR/${BOT_ID}-prompt.md"

    generate_bot_prompt "$CURRENT_PR" "$WORKTREE" "$BOT_ID" "$BOT_HANDOFF" > "$BOT_PROMPT_FILE"

    cd "$WORKTREE" || continue
    nohup pi --approve \
      --session-id "$BOT_ID" \
      --skill "$BOT_SKILL_PATH" \
      -p "@$BOT_PROMPT_FILE" \
      > "$LOG_DIR/${BOT_ID}.log" 2>&1 &
    BOT_PID=$!
    CHILD_PIDS+=("$BOT_PID")
    CURRENT_AGENT_PID="$BOT_PID"
    write_status "running"
    log "  Agent PID $BOT_PID (session: $BOT_ID)"

    if ! wait_for_handoff "$BOT_HANDOFF" "$TIMEOUT_BOT" "$BOT_PID"; then
      log "  WARNING: Timed out or died. Attempting merge anyway."
      kill_agent "$BOT_PID"
    else
      log "  Done"
      # Check for explicit bot-review blockers. CI status is verified in the merge phase;
      # do not infer CI failure from free-form handoff prose such as "all green after retry".
      if grep -Eiq "Exit reason:.*blocker" "$BOT_HANDOFF" 2>/dev/null || \
        (grep -Eiq "Still blocked:" "$BOT_HANDOFF" 2>/dev/null && ! grep -Eiq "Still blocked:[[:space:]]*(none|0|zero|$)" "$BOT_HANDOFF" 2>/dev/null); then
        log "  Bot reports a review blocker. Skipping merge."
        handle_issue_failure "$ISSUE" "bot review reported a blocker" || break
        continue
      fi
    fi
  fi

  # ── Phase 5: Merge ───────────────────────────────────
  if [ "$NO_MERGE" = "1" ]; then
    log "[5/5] Merge: SKIPPED (NO_MERGE=1). PR #$CURRENT_PR ready."
    ISSUES_COMPLETED+=("$ISSUE")
    continue
  fi

  CURRENT_PHASE="merging"
  write_status "running"
  log "[5/5] Merge PR #$CURRENT_PR"
  cd "$REPO" || continue

  # Poll CI until complete
  CI_FAILURES=$(wait_for_ci "$CURRENT_PR" "$TIMEOUT_CI") || true

  if [ "$CI_FAILURES" = "0" ]; then
    # Draft PRs cannot be merged even when checks/reviews are green. Mark ready before merging.
    if [ "$(gh pr view "$CURRENT_PR" --json isDraft -q .isDraft 2>/dev/null || echo false)" = "true" ]; then
      log "  PR is draft; marking ready before merge"
      gh pr ready "$CURRENT_PR" 2>/dev/null || true
    fi

    # Remove worktree before merge so --delete-branch works
    cleanup_worktree "$WORKTREE"

    if gh pr merge "$CURRENT_PR" --"$MERGE_STRATEGY" --delete-branch --admin 2>/dev/null; then
      log "  Merged via gh CLI ✓"
    elif gh api "repos/$OWNER_REPO/pulls/$CURRENT_PR/merge" -X PUT \
        -f merge_method="$MERGE_STRATEGY" \
        -f commit_title="$(gh pr view "$CURRENT_PR" --json title -q .title) (#$CURRENT_PR)" 2>/dev/null; then
      log "  Merged via API ✓"
      gh api "repos/$OWNER_REPO/git/refs/heads/$BRANCH" -X DELETE 2>/dev/null || true
    else
      log "  ERROR: Merge failed."
      handle_issue_failure "$ISSUE" "merge failed" || break
      continue
    fi
  elif [ "$CI_FAILURES" = "timeout" ]; then
    log "  ERROR: CI timed out. Skipping merge."
    cleanup_worktree "$WORKTREE"
    handle_issue_failure "$ISSUE" "CI timed out" || break
    continue
  else
    log "  ERROR: CI has $CI_FAILURES failures. Skipping merge."
    cleanup_worktree "$WORKTREE"
    handle_issue_failure "$ISSUE" "CI has $CI_FAILURES failures" || break
    continue
  fi

  # Pull base branch for next issue
  cd "$REPO" || continue
  git fetch origin "$BASE_BRANCH" --quiet
  git pull origin "$BASE_BRANCH" --ff-only 2>/dev/null || true
  ISSUES_COMPLETED+=("$ISSUE")
  CURRENT_PHASE=""
  log "  Issue #$ISSUE done ✓"
  write_status "running"
  sleep 10
done

# ── Clean exit ───────────────────────────────────────────────────────────────
trap - INT TERM
write_status "$PIPELINE_TERMINAL_STATE"

log ""
log "═══════════════════════════════════════════════════════════════"
log "PIPELINE COMPLETE"
log "═══════════════════════════════════════════════════════════════"
log "Results:"
for issue in "${ISSUES_COMPLETED[@]:-}"; do
  [ -n "$issue" ] && log "  ✓ #$issue merged"
done
for issue in "${ISSUES_SKIPPED[@]:-}"; do
  [ -n "$issue" ] && log "  ✗ #$issue skipped"
done
log ""
log "${#ISSUES_COMPLETED[@]} merged, ${#ISSUES_SKIPPED[@]} skipped."
